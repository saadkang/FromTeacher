package ArraysUnderMind;

public class ArraysInAction {

	public static void main(String[] args) {
		String[] peopleInHere = {"Belee", "KangstA", "Andrea", "MaryAnn", "Charlene"};
		String[] contactInfo = {"MA", "NC", "PA","CA", "TX"};
		
		for(int x = 0; x < peopleInHere.length ;x++){
			System.out.println(peopleInHere[x]);
			for(int y = 0; y < 1; y++){
				System.out.println(contactInfo[y]);
		}
		}

	}

}
