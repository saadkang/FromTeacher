package thisIsFullOfNotesToComeBackAndReview;

public class MainLoop {

	public static void main(String[] args) {
		/* Every time you need to write a loop you need to write in the format
		 * 'for(counter variable; condition; increment){This is where you
		 * write something}'. There are 3 parts to loop separated by ';' goes inside
		 * the brackets after the word 'for', first part is called 'counter variable'
		 * in this part you will declare and initilize the variable and the second
		 * part is called 'condition' in this part you want to tell java when to stop
		 * running the loop, and third part is changing the value of the variable so 
		 * the loop will stop, if you don't put the third part in, java will not change
		 * the value for the variable and I think the loop will not stop*/
		for(int i = 0; i < 5; i++){
		
			System.out.println(i);
		}

	}

}
